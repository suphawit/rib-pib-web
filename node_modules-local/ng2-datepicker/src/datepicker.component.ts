  /**
  * Validates that a value is within the 'rangeStart' and/or 'rangeEnd' if specified
  */
  inRangeValidator(control: FormControl): ValidationResult {
    const value = control.value;

    if (this.currentMonthNumber) {
      const tentativeDate = new Date(+value, this.currentMonthNumber);
      if (this.rangeStart && tentativeDate.getTime() < this.rangeStart.getTime()) {
        return { 'yearBeforeRangeStart': true };
      }
      if (this.rangeEnd && tentativeDate.getTime() > this.rangeEnd.getTime()) {
        return { 'yearAfterRangeEnd': true };
      }
      return null;
    }

    return { 'currentMonthMissing': true };
  }

  /**
  * Validates that a value is a number greater than or equal to 1900
  */
  yearValidator(control: FormControl): ValidationResult {
    const value = control.value;
    const valid = !isNaN(value) && value >= 1900 && Math.floor(value) === +value;
    if (valid) {
      return null;
    }
    return { 'invalidYear': true };
  }

  get dicMonth() {
    let months: Dictionary = {};

    for (let i = 0; i <= 11; i++) {
      let newDateValid = false;
      let obj = { id: i, displayName: this.months[i], disabled: false };

      try {
        let newDate = new Date(this.currentYear, i);
        newDateValid = (!this.rangeStart || moment(newDate).endOf('month').toDate().getTime() >= this.rangeStart.getTime()) &&
          (!this.rangeEnd || newDate.getTime() <= this.rangeEnd.getTime());
      } catch (ex) {
        console.log("Exception occur:\n", ex);
      }

      obj.disabled = !newDateValid;
      months[i] = obj;
    }

    return months;
  }

  get monthList(): any {
    return this.dicMonth != null ? Object.keys(this.dicMonth) : [];
  }

  get yearList() {
    let years = [];
    let startYear = 2005;
    let endYear = moment().year();

    if (this.rangeStart) {
      startYear = moment(this.rangeStart).year();
    }
    if (this.rangeEnd) {
      endYear = moment(this.rangeEnd).year();
    }

    for (let i = startYear; i <= endYear; i++) {
      years.push(i);
    }

    return years;
  }

  get daysNames() {
    let daysNameList = [];
    for (let i = 0; i < 7; i++) {
      daysNameList.push(moment().weekday(i).format('ddd'));
    }
    return daysNameList;
  }
